  <p>In the 1990’s a new musical movement was developing in Cuba, creating a new music called Timba. This was an update of their traditions, bringing in new influences, new harmonies and new arrangements to the Cuban sound.  At the same time in London, there was a re-birth of a thriving live Latin scene, spurred on by the release of the Buena Vista Social Club, and the influx of Cubans moving to the UK. Emerging from these two scenes was the band Motimba – giving Timba a London sensibility – creating a new hybrid - Timba Funk. </p>

