  <h1 class="albumo">Justin Thurgur</h1>

 <section>
 <img class="artistspage__image" src="../assets/images/artistspage/JustinThurgur/JustinThurgur.jpg">

 <div class="artists__text">

<p>Justin Thurgur is most well known for his work with the seminal English folk group Bellowhead, who he played with from its formation in 2004 until they called it a day in May 2016. Bellowhead were winners of five ‘Best Live Act’ awards, two ‘Best Group’ awards and one ‘Best Album’ award from the BBC Radio 2 Folk Awards. They were Artists in Residence at the South Bank Centre from 2007. In 2014 they signed to Island Records for their fifth full album ‘Revival’.</p>

  <p>Thurgur has been collaborating with Kishon Khan since 1996. This collaboration started with Khan’s AfroLatinJazz group ‘The Bonobo Orchestra’ and then after some earlier explorations in to Cuban music led to the formation of their CubanFunk group ‘Motimba’, in 1998. More recently they have been working together in Khan’s BanglaAfroLatinJazz group ‘Lokkhi Terra’ and in Thurgur’s own AfroJazz project. They have also recorded a Bengali Dub EP, ‘Bangla Rasta’, with Baul singers Baby Akhtar and Rob Fakir, a Bengali Jazz album with Akhtar and they have formed a group combining Lokkhi Terra with the Afro-Beat maestro Dele Sosimi called ‘Cubafrobeat’, which combines Cuban Rhumba with Afro-Beat.</p>

  <p>In 2010 Khan and Thurgur went to Cuba to record an album in Egrem studios with Indian percussionist Pandit Dinesh, which includes the likes of Giraldo Piloto, Changuito, Julito Padron and Hariharan.</p>
<p>Thurgur appeared on a soundtrack of Khan’s for the Bangladeshi movie ‘The Last Thakur’, for which Khan won a Grand Jury award for ‘Best Music’ at the South Asian International Film Festival in New York.</p>
<p>The creation of Funkiwala Records by Khan and Thurgur was inspired by a desire to draw attention to a community of musicians who, like them, have spent their lives immersing themselves in different musical cultures and coming together to share this collective experience.</p>
<p>This commitment to the idea of sharing and shared musical communication, breaking down borders and creating music that celebrates commonality has been a defining feature of Thurgur’s career. It has been as true of the approach of a group like Bellowhead to the English folk music tradition as to Lokkhi Terra, placing Bengali folk songs with the rhythm section and horn sounds of Cuba, Africa and beyond.</p>
<p>Since 1998 Thurgur has also been collaborating in Dele Sosimi’s own projects, first in the group Gbedu Resurrection and then in Sosimi’s Afro-Beat Orchestra. For Sosimi’s first two albums and one they did together for the Nigerian poet Ikwunga, Thurgur also acted as co-producer, arranger/composer and recording engineer. Sosimi’s fourth album, ‘You No Fit Touch Am’, was released with Wah Wah 45’s in 2015.</p>
<p>Along with his collaborations with Khan and Sosimi, Thurgur has also worked with the likes of Tony Allen, Damon Albarn’s Africa Express project (which specifically included work with Tony Allen, Cheick Tidiane-Seck and Fatoumata Diowara), Tony Kofi, Bukky Leo, Wara, The Soothsayers, Inemo, Osvaldo Chacon, Roberto Pla, Javier Camilo, The Selecter and The Levellers. He was in the band for the National Theatre production of ‘Fela’.</p>





