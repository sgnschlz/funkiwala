    <p>“Border breaking communication.......deftly celebrates our commonality.” <br />
      4-­star review, Jane Cornwell – Evening Standard</p>

    <p>‘No Confusion’ is Justin Thurgur’s debut album as a leader. Written in collaboration with the double bassist Max De Wardener and pianist Kishon Khan. <br />
      The album combines all the influences from Thurgur’s musical journey. As a kid inspired by the likes of John Coltrane, Wayne Shorter, Miles Davis, Lee Morgan and Fred Wesley he has since spent nineteen years as the right hand man to Afro-Beat maestro Dele Sosimi and twenty two years collaborating with Kishon Khan, mixing Cuban music with various music from the African continent, also exploring the music of Brasil, Reggae, Bengali Folk and of course Jazz.
    </p>
    <p>This is on the surface an Afro-Jazz album but scratch the surface and the influences run deep.</p>
    <p>Thurgur is most known as the trombonist from the seminal folk group Bellowhead but for over twenty years he has also been at the heart of a wider international scene centred in London. As well as his long term collaborations with Sosimi and Khan he has also worked with such luminaries as Tony Allan, Giraldo Piloto, Julito Padron, Changuito, Pandit Dinesh, Cheick Tidiane-Seck, Fatoumata Diowara, Nozrul Islam, Tony Kofi, The Levellers and The Selecter.</p>
    <p>As well as Thurgur, Khan and de Wardener, the album features Graeme Flowers and Tom Allan on trumpet and flugel horn, James Allsopp on bass clarinet, Phil Dawson on guitar, Oreste Noda on congas, Pat Illingworth on drums, Jimmy Martinez on baby bass, Javier Camillo on bongos, Rob Leake on baritone and tenor sax, Ben Somers on baritone and tenor sax, Eric Rohner on tenor sax, Tamar Osborn on baritone sax, Maurizio Ravalico and Dave Pattman on percussion and Leo Taylor on drums.</p>
      

