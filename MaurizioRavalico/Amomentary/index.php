<p>
    First Funkiwala release of visionary percussionist Maurizio Ravalico is this genre-promiscuous, loosely dj-oriented EP 12” vinyl EP, which funnels his recent collaborations with the Fiium Shaarrk trio and Tamar Osborn’s Collocutor into an introduction and a compendium to his solo percussion album, “Nobody’s Husband, Nobody’s Dad”, out on Funkiwala in November 2018.
