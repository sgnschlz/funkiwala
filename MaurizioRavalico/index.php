 <div class="artists__text">
 <p> Throughout a carreer spanning over 30 years, Maurizio Ravalico has maintained lively and inter-breeding interests in musical expressions as diverse as funk, prog-rock, 20th century composition, Hispano-American and Brazilian music, free-form improvisation, contemporary jazz and the still-craving-for-a-better-name "experimental music" of post-war and beyond. </p>

  <p>He studied Afrocuban percussion in La Havana, where he built his solid fundations as a conga player, and during the last twelve years, following his experiences in improvisation, he has developed a very personal vocabulary in both his conga playing and the original set-up which he employs in his solo act and in many of his current collaborations.</p>

 <p>Based in London since 1991, he has toured and recorded with, among others, Jamiroquai, Paul McCartney, James Taylor, Jesus Alemani, Snowboy, Alex Wilson, Dennis Rollins, Greg Osby, Kaidi Tatham, Dego McFarland, Finn Peters, John Edwards, Steve Beresford, Oren Marshall, Shabaka Hutchings, Andrea Parkins, and the choreographers Lea Anderson, Bill T. Jones, Maja Garcìa and Jane Turner.</p>
 
<p>The last album of his Anglo-Berliner trio Fiium Shaarrk, “We Are Astonishingly Lifelike” was one of BBC3 Late Junction’s “12 Best of 2017”, and among the best 50 in Gilles Peterson’s 50 Essential Jazz and Beyond Records of 2017. He is currently a member of the Dele Sosimi Afrobeat Ensemble, Tamar Osborn’s Collocutor, Phil Dawson’s Trio, a collaborator of the Phaedra Ensemble,
as well as performing material from his solo percussion album 'Nobody's Husband, Nobody's Dad.</p>

<p>E-mail: here@maurizioravalico.com</p>
 
 </section>

