<p> “The group presenting this charming collection of Bangladeshi music lives up to its name.It is indeed an all-star cast....the resulting music is undeniably beautiful” Songlines Magazine.</p>
              <p> “Performed beautifully and with exquisite taste” Dhaka Tribune. </p>

              <p>Bangladesh is not only a land of Rivers, it’s a land of song. Ancient songs, full of poetry and spiritual meaning, can be heard everywhere. From popular boat songs to the great works of the mystic poet Lalon Shah, these are living traditions that continue to inspire each new generation.</p>

              <p>Funkiwala wanted to present a ‘roots revisited’ album, filled with the sounds you hear across Bangladesh, but rarely on the international scene. With this in mind we approached Bangladesh’s top Dhol maestro, and my personal friend, Nazrul Islam.
                Nazrul has performed and recorded with virtually every major artist in the country. From popular Bengali folk to Baul, Bangla rock fusion to Bangla Afro Latin jazz – he’s done it all, and is a living legend. I asked him to put together a super group, to represent the traditional sounds of Bangladesh.</p>

              <p>What he put together was a group of Dons – On flute Jalal Ahmed, and on doitara Mokaddes Ali – both musical heavyweights on the scene, zooming around on their motorbikes through bombs and hartals (political strikes) to get to our sessions. On Dupki and tablas is Bipu, forever smiling, and on Ektara, Biju, forever being naughty. Directing proceedings from his dhol, the man himself, Nazrul.
                To sing, he chose a trio who are the finest in their genres, as well as long time collaborators on numerous projects. </p>

              <p>Baul Rob Fakir comes from Kushtia (birthplace of Lalon Shah). There’s a lot of talk about the bauls, and how they follow the spiritual teachings of Lalon. Well Rob is the real deal. He has an incredible aura, befitting of a spiritual man, and a truly hypnotic quality to his singing, which can best be described as mesmerising.
                Representing the next generation is Labik Kamal Gaurob – multi-instrumentalist, singer/songwriter, producer, arranger, and also survivor (after blowing himself up in his energy-efficient car – gas leak).</p>

              <p>Completing the front line is the incredible Dewan Baby Akhtar – folk singer, violin player, incredible swing, consummate professional and also the wife of Nazrul - the real boss. </p>

              <p>Joining Kishon Khan from London to help produce was Tansay Omar, who also brought along a portable studio which we set up to do informal overdubs. Recording the main sessions in his new studio “Echo Bass” in Dhaka was engineer and fellow pianist Saad Chowdhury. Together the three of them were taken through a master class of Bengali sounds – travelling in the company of legends.</p>

