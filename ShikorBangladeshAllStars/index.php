<p> Shikor means “roots” in Bengali. The Shikor  Bangladesh AllStars were put together by the country’s premiere Dhol maestro (percussionist), Nazrul Islam, to unite some of Bangladesh’s leading folk exponents in one group, and to take the music out to a wider audience. Shikor hit the international stage at Womad2015 and Songlines Encounters Festival 2015, with no idea of how their music would be received by audiences who don’t speak Bengali. Here are just some of the reactions 
“One of the highlights of the festival” Lopa Kothari, BBC Radio 3, Womad “seriously incredible rhythms, incredible vocals, and later (in workshop with 
LoKkhi TeRra) awesome cross-cultural collaboration” – London Jazz, Womad review 
“Considering they were totally acoustic, Shikor Bangladesh AllStars had plenty of power on the Charlie Gillett stage. Delivering traditional Bangladeshi folk songs with dhol drums, harmonium and other customary instruments, lead singer Baby Akhtar belted out some meaty vocals, but the really chilling element was the bamboo flute played with skill...” United Reggae, Womad review </p>

<p>“My next personal favourite was the Shikor Bangladesh AllStars – a tradional folk band from Bangladesh, dressed mostly in flowing white, playing an exquisite collection of traditional instruments – strings, large double ended and psychedelic coloured drums, flutes, bells and rattles – accompanied by soaring and emotive vocals and rhythmic beats and claps. The flutes seemed to transport you beyond – to a place of intoxicating enchantment – and complete with a female vocalists stirring incantations, were reminiscent of India’s devotional bhajan music” Life Arts Media 
The band boasts a line-up of some of Bangladesh’s finest. Nazrul  himself has recorded and performed with almost every major star in the country, from the folk divas such as Mumtaz to the new generation folk-rock stars such as Arnob to backing the Baul community – he is on countless commercial mix-tapes found nationally and throughout the diaspora. On Bamboo flute is Jalal Ahmed, widely regarded as the country’s best, as is Dotara player Mokaddes Ali. A trio of vocalists form the front line – Rob Fakir is one of Bangladesh’s most loved Bauls; Baby Akhtar is seen as one of the rising stars of the folk scene; as is vocalist/multi-instrumentalist Labik Kamal Gaurob, here representing the next generation keeping in touch with their roots.</p>


